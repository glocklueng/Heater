void set_pwm(float pwm_value);
float pid(float t_meas, float t_set)
{
	static int error_sum = 0;
	static float prev_diff = 0;
	float diff = t_set - t_meas;
	float d_diff = (prev_diff - diff) / DIFF_TIME_INTERVAL;
	prev_diff = diff;

	float result_pwm = 0;
	if (btn_short_pressed)
	{
		error_sum += diff;
		result_pwm = K_p*diff + K_i*error_sum + K_d*d_diff;

		if (error_sum > MAX_TEMP * 2)
		{
			error_sum = MAX_TEMP;
		}
		if (error_sum < -MAX_TEMP * 2)
		{
			error_sum = -MAX_TEMP;
		}
		if (result_pwm > 1.0)
		{
			result_pwm = 1.0;
		}
	}
	if (!btn_short_pressed)
	{
		result_pwm = 0.0;
	}
	return result_pwm;
}
